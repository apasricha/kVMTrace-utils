#! /usr/bin/env python3



# ==================================================================================================================================
# IMPORTS

import sys
# ==================================================================================================================================



# ==================================================================================================================================
# The entry point.

def main (argv):
    for arg in argv:
        print(arg);
# ==================================================================================================================================



# ==================================================================================================================================
# Trigger to call main() when invoked from the command line, passing the command-line arguments.

if __name__ == "__main__":
   main(sys.argv[1:])
# ==================================================================================================================================
